function out= experiment2(t,y,m,k1,a1,a2)
%参数
g=9.806;
k2=0.0003;
l=0.1861;
M=0.57;
ng=1;
nm=1;
kg=3.71;
kt=0.00767;
km=0.00767;
rm=2.6;
rmp=0.00635;
I=4/3*m*l^2;
V=4*sin(4.2*t);
%力
F1=ng*kg*kt/(rm*rmp)*(-kg*km*y(2)/rmp+nm*V);
F=a1*(F1+a2);
%状态方程矩阵
m11=M+m;
m12=m*l*cos(y(3));
m21=m*l*cos(y(3));
m22=I+m*l^2;
c12=-m*l*sin(y(3))*y(4);
g21=m*g*l*sin(y(3));
detM=m11*m22-m21*m12;
%模型输出
y1=m22/detM*(F-c12*y(4)-k1*y(2))-m21/detM*(-k2*y(4)-g21);
y2=-m12/detM*(F-c12*y(4)-k1*y(2))+m11/detM*(-k2*y(4)-g21);
out=[y(2);y1;y(4);y2];